async function run() {
  console.log('🌱 Seeding PASEO database with demo data...\n');

  const url = `https://api.supabase.com/v1/projects/${PROJECT_REF}/database/query`;

  const res = await fetch(url, {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${ACCESS_TOKEN}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ query: SQL }),
  });

  if (!res.ok) {
    const text = await res.text();
    console.error(`❌ Failed (${res.status}): ${text}`);
    process.exit(1);
  }

  const result = await res.json();
  if (result.error) {
    console.error('❌ SQL Error:', result.error);
    process.exit(1);
  }

  console.log('✅ Demo data seeded successfully!\n');
  console.log('   👥 5 ספקים');
  console.log('   📄 10 חשבוניות');
  console.log('   📦 23 פריטים');
  console.log('   📊 13 רשומות היסטוריית מחירים (4 שינויי מחיר + 5 מוצרים חדשים)');
  console.log('\n🎉 Open your app to see the data!');
}

run().catch(err => {
  console.error('❌ Error:', err.message);
  process.exit(1);
});
